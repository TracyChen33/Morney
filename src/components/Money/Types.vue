<template>
  <ul class="types">
    <li :class="type === '-' && 'selected'" @click="selectType('-')">Spend</li>
    <li :class="type === '+' && 'selected'" @click="selectType('+')">Income</li>
  </ul>
</template>

<script>
  export default {
    name: 'types',
    props:['xxx'],
    data() {
      return {
        type: '-' // '-' for spend, '+' for Income
      }
    },
    mounted(){
      console.log(this.xxx)
    },
    methods: {
      selectType(type) {
        if (type !== '-' && type !== '+') {
          throw new Error('type is unknown')
        }
        this.type = type
      }
    }
  }
</script>

